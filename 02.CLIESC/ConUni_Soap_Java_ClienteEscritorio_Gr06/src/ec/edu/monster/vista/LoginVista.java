    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
    // Obtiene los valores ingresados en los campos de texto
    String usuario = txtUsuario.getText();
    String clave = txtClave.getText(); // Convierte el password a String

    try {
        // Llama al servicio web para verificar las credenciales
        ConversionUnidadesControlador control = new ConversionUnidadesControlador();
        boolean loginExitoso = control.login(usuario, clave);

        if (loginExitoso) {
            // Credenciales correctas: abre la vista ConversionVista
            ConversionVista conversionVista = new ConversionVista();
            conversionVista.setVisible(true);
            this.dispose(); // Cierra la ventana de login
        } else {
            // Credenciales incorrectas: muestra un mensaje de error
            lblMensaje.setText("Credenciales incorrectas.");
            txtUsuario.setText(""); // Limpia el campo de usuario
            txtClave.setText("");   // Limpia el campo de contraseña
            txtUsuario.requestFocus(); // Enfoca nuevamente en el campo de usuario    
        }
    } catch (Exception e) {
        e.printStackTrace();
        lblMensaje.setText("Error al conectar con el servicio.");
    }
    }//GEN-LAST:event_btnIngresarActionPerformed

